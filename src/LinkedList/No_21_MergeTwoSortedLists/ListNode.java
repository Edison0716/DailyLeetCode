public class ListNode {
    int val;
    ListNode next;
    public ListNode(int x) { val = x; }

    public void setNext(ListNode next) {
        this.next = next;
    }

    public int getVal() {
        return val;
    }

    public ListNode getNext() {
        return next;
    }
}

